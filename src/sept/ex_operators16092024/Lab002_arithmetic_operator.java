package sept.ex_operators16092024;

public class Lab002_arithmetic_operator {
    public static void main(String[] args) {
        //Arithmetic operator
        // =,-,/,*,%

        int a=10;
        int b=2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
