package sept.ex_25092024_Loops1;

public class Lab010_loopswithconditions {
    public static void main(String[] args) {

        for(int i=1; i<=10; i++) {
            if (i==5) {
                System.out.println("Five");
            }else{
                System.out.println(i);
            }
        }
    }
}
